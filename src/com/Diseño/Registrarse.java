        if (txtCorreo.getText().toString().equals("Correo") || txtNombre.getText().toString().equals("Nombre")
                || txtApellido.getText().toString().equals("Apellido")
                || String.valueOf(psContraseña.getPassword()).equals("***************")) {

            JOptionPane.showMessageDialog(this, "Uno o mas campos estan vacios, intente otra vez.");

        } else {
            if (lblAviso.isVisible() == true) {
                JOptionPane.showMessageDialog(null, "Escriba el correo correctamente");
            } else {
                try {

                    ps = connect.prepareStatement("INSERT INTO Usuarios (nombre, apellido, correo, rol, password) VALUES (?,?,?,?,?)");
                    ps.setString(3, txtCorreo.getText());
                    ps.setString(1, txtNombre.getText());
                    ps.setString(2, txtApellido.getText());
                    ps.setString(5, String.valueOf(psContraseña.getPassword()));
                    if (CBSeleccionarRol.getSelectedIndex() == 0) {
                        //ps = connect.prepareStatement("INSERT INTO Usuarios (rol) VALUES (?)"); 
                        ps.setString(4, "Estudiante");
                    } else {
                        //ps = connect.prepareStatement("INSERT INTO Usuarios (rol) VALUES (?)"); 
                        ps.setString(4, "Profesor");
                        //ps.setInt(4, CBSeleccionarRol.getSelectedIndex());
                    }
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(rootPane, "Registro realizado con exito.");
                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(rootPane, "Error en la consulta: " + ex);
                } finally {
                    try {
                        if (ps != null) {
                            ps.close();
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, e);
                    }
                }
            }
            if (lblAviso.isValid() == false) {
                log.setVisible(true);
                this.dispose();
            }
        }


