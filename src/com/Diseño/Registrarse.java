    private void txtNombreMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtNombreMousePressed
        //Da efecto de PlaceHolder a la caja de texto.
        if (txtCorreo.getText().isEmpty()) {
            txtCorreo.setText("Correo");
            txtCorreo.setForeground(new Color(102, 102, 102));
        }
        if (txtNombre.getText().equals("Nombre")) {
            txtNombre.setText("");
            txtNombre.setForeground(Color.white);
        }
        if (txtApellido.getText().isEmpty()) {
            txtApellido.setText("Apellido");
            txtApellido.setForeground(new Color(102, 102, 102));
        }
        if (String.valueOf(psContraseña.getPassword()).isEmpty()) {
            psContraseña.setText("***************");
            psContraseña.setForeground(new Color(102, 102, 102));
        }
        if (txtCorreo.getText().equals("Correo")) {
            lblAviso.setVisible(false);
        }
    }//GEN-LAST:event_txtNombreMousePressed

